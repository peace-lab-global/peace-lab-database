def translate_filename(name):
    """Translate an English filename to Chinese using word-level dictionary."""
    # Split by underscore and translate each part
    parts = name.split('_')
    translated_parts = []

    i = 0
    while i < len(parts):
        # Try multi-word match first (greedy)
        matched = False
        for length in range(min(4, len(parts) - i), 0, -1):
            multi = '_'.join(parts[i:i+length])
            if multi in WORD_TRANSLATIONS:
                translated_parts.append(WORD_TRANSLATIONS[multi])
                i += length
                matched = True
                break
        if not matched:
            word = parts[i]
            if word in WORD_TRANSLATIONS:
                translated_parts.append(WORD_TRANSLATIONS[word])
            else:
                # Keep original if no translation found
                translated_parts.append(word)
            i += 1

    return ''.join(translated_parts)
